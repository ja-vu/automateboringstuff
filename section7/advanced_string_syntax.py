"""
Escape characters
\' == Single Quote
\" == Double quote
\t == Tab
\n == Newline (line break)
\\ == Backslash
"""

print('hello')
print("That is Alice's cat.")
print('Say hi to Bob\'s mother')
print('Hello there! \nHow are you?\nI\'m fine')

# Raw String
print(r'Hello')
print(r'That is Carol\'s cat.')

# Multi line string
print("""Dear Alice,
blah blahblah
love you
Sincerely,
""")